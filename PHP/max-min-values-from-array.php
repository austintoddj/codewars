<?php

/**
 * Return the highest value from a given array.
 *
 * @param array $array
 *
 * @return string
 */
function maximum($array) {
    
    return max($array);
}

/**
 * Return the lowest value from a given array.
 *
 * @param array $array
 *
 * @return string
 */
function minimum($array) {

    return min($array);
}